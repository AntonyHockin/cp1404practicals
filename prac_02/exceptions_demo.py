# 1. ValueError occurs when a character that isn't an integer is entered
# 2. ZeroDivisionError occurs when the denominator is zero
# 3. A while loop could be used so that the denominator is asked for until a valid answer is given

try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Cannot divide by zero!")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
print("Finished.")
